var color = 'magenta';
switch (color) {
    case 'blue':
        console.log("this is blue");
        break;
    case 'white':
        console.log("this is white");
        break;
    case 'black':
        console.log("this is black");
        break;
    case 'green':
        console.log("this is green");
        break;
    case 'yellow':
        console.log("this is yellow");
        break;
    default:
        console.log("No color found");
}